// Harita boyutlandƒ±rma bile≈üeni
const MapResizer: React.FC<{ onMapReady: (map: L.Map) => void }> = ({ onMapReady }) => {
  const map = useMap();

  useEffect(() => {
    // Map referansƒ±nƒ± √ºst bile≈üene g√∂nder
    onMapReady(map);

    const resizeMap = () => {
      // Harita konteynerinin boyutlarƒ±nƒ± yeniden hesapla
      setTimeout(() => {
        map.invalidateSize();
      }, 100);
    };

    // Sayfa y√ºklendiƒüinde ve pencere boyutu deƒüi≈ütiƒüinde haritayƒ± yeniden boyutlandƒ±r
    resizeMap();
    window.addEventListener('resize', resizeMap);

    return () => {
      window.removeEventListener('resize', resizeMap);
    };
  }, [map, onMapReady]);

  return null;
};

